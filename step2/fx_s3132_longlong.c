fixed32 fx32_sind(fixed32 fa)
{
    int sign = 1;   
    fixed32 ret0, diff;
    int idx; 
    if ( fa < 0 ) 
    {
        sign = -1;
        fa *= -1; 
    }
    fa = fa % _FX_S3132_360; 
    if ( fa >= _FX_S3132_180 ) 
    {
        sign *= -1;
        fa -= _FX_S3132_180;
    }
    if ( fa > _FX_S3132_90 ) 
        fa = _FX_S3132_180 - fa;
    idx = fa>>16; 
    ret0 = fx_s3132_SinTable[idx]; 
    diff = fx_s3132_SinTable[idx+1]-ret0;
    return ( sign *( ret0 + ((diff*(fa&0xFFFF))>>16) )); 
}

